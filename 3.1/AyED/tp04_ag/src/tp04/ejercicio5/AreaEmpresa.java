package tp04.ejercicio5;

public class AreaEmpresa {
	private int tiempo;
	private String id;
	
	
	public AreaEmpresa(int tiempo, String id) {
		this.tiempo = tiempo;
		this.id = id;
	}


	public int getTiempo() {
		return tiempo;
	}


	public void setTiempo(int tiempo) {
		this.tiempo = tiempo;
	}


	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}
	
	
}
